List p=18f4520
    #include<p18f4520.inc>
        CONFIG OSC = INTIO67
        CONFIG WDT = OFF
        org 0x00
	

	LFSR 0, 0x200
	LFSR 1, 0x210
	LFSR 2, 0x220
	
	MOVLW 0x00 ;A1
	MOVWF POSTINC0
	MOVLW 0x33 ;A2
	MOVWF POSTINC0
	MOVLW 0x58 ;A3
	MOVWF POSTINC0
	MOVLW 0x7A ;A4
	MOVWF POSTINC0
	MOVLW 0xC4 ;A5
	MOVWF POSTINC0
	MOVLW 0xF0 ;A6
	MOVWF POSTINC0
	LFSR 0, 0x200
	
	MOVLW 0x09 ;B1
	MOVWF POSTINC1
	MOVLW 0x58 ;B2
	MOVWF POSTINC1
	MOVLW 0x6E ;B3
	MOVWF POSTINC1
	MOVLW 0xB8 ;B4
	MOVWF POSTINC1
	MOVLW 0xDD ;B5
	MOVWF POSTINC1
	LFSR 1, 0x210
	
	MOVLW 6
	MOVWF 0x1F0
	
	MOVLW 5
	MOVWF 0x1F1
	
sort:   
	CLRF WREG
	ADDWF INDF0, W ;Take A
	CPFSLT INDF1; ;if B < A -> skip next line
	GOTO PUTA
	GOTO PUTB
	
	
PUTA:
	CLRF WREG
	ADDWF POSTINC0, W
	MOVWF POSTINC2
	DECFSZ 0x1F0
	GOTO sort
	GOTO FINISHB
	
	
PUTB:
	CLRF WREG
	ADDWF POSTINC1, W
	MOVWF POSTINC2
	DECFSZ 0x1F1
	GOTO sort
	GOTO FINISHA
	

FINISHA:
	CLRF WREG
	ADDWF POSTINC0, W
	MOVWF POSTINC2
	DECFSZ 0x1F0
	GOTO FINISHA
	GOTO STOP
	
	
FINISHB:
	CLRF WREG
	ADDWF POSTINC1, W
	MOVWF POSTINC2
	DECFSZ 0x1F1
	GOTO FINISHB
	GOTO STOP
    
STOP:
	end